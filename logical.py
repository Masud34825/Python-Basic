print("And Operation x(10)!=y(20) And x(10)<y(20) is: ",x!=y and x<y)
print("OR Operation x(10)==y(20) or x(10)>y(20) is: ",x==y or x>y)
print("OR Operation x(10)!=y(20) And x(10)<y(20) is: ",x!=y or x<y)
