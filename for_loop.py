print("For Loop In Range 1 Parameter")
for i in range(10):
    print(i)
    
print("For Loop In Range 2 Parameter")

for i in range(1,10):
    print(i) 

print("For Loop In Range 3 Parameter 2 increments")

for i in range(1,10,2):
    print(i)
    
print("For Loop In Range reverse order")

for i in range(10,0,-1):
    print(i)

